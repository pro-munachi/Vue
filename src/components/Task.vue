<template>
  <div
    @dblclick="$emit('toggle-reminder', task.id)"
    :class="[task.reminder ? 'reminder' : '', 'task']"
  >
    <h3>
      {{ task.text }}
      <p class="x" @click="onDelete(task.id)">x</p>
    </h3>
    <p>
      {{ task.day }}
    </p>
  </div>
</template>

<script>
export default {
  name: "TaskComponent",
  props: {
    task: Object,
  },

  methods: {
    onDelete(id) {
      this.$emit("delete-task", id);
    },
  },
};
</script>

